          .h6 Acción comunal
          p Es una expresión social organizada, autónoma y solidaria de sociedad civil, tiene como propósito promover el desarrollo integral, sostenible y sustentable construido a partir del ejercicio de la democracia participativa en la gestión del desarrollo de la comunidad.  

        .col-10.bg-5
          .h6 Asociaciones
          p Son organizaciones sin ánimo de lucro conformadas por personas con intereses comunes. No tienen número mínimo o máximo de afiliados, son de libre adhesión y se regulan plenamente por sus estatutos. 

        .col-10.bg-5
          .h6 Asociaciones mutuales
          p En una mutualidad se cumple el principio de identidad o unidad, característico de las empresas de participación, realizándose además la actividad principal exclusivamente con los socios. Se incluye la participación democrática, la igualdad de derechos y obligaciones, y la asignación del excedente económico.  

        .col-10.bg-5
          .h6 Cooperativas
          p Son una sociedad constituida por personas que se asocian en régimen de libre adhesión y baja voluntaria para la realización de actividades empresariales, encaminadas a satisfacer sus necesidades y aspiraciones económicas y sociales, sin ánimo de lucro. Atienden a principios cooperativos, aceptados y regulados, entre los que encontramos la adhesión voluntaria y abierta, la participación económica, la educación, formación e información, el interés por la comunidad, la autonomía e independencia, y la intercooperación. Todos estos principios están directamente relacionados con el cooperativismo, y se clasifican en:
