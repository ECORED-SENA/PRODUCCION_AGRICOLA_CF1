        .col-10.bg-5
          .h6 Corporaciones
          p Son organizaciones sin ánimo de lucro que buscan alcanzar un objetivo social. Sus miembros no requieren vínculo común, no existe valor mínimo en sus aportes y se regulan totalmente por sus propios estatutos. 

        .col-10.bg-5
          .h6 Empresas de inserción y de integración
          p Consisten en iniciativas empresariales que combinan la lógica empresarial con metodologías de inserción laboral que buscan potencializar los servicios a las personas y favorecer la calidad de vida, producen bienes y servicios que les permite ser más rentables y competitivos; así como también integrar a personas en situación de exclusión. Son estructuras de aprendizaje que buscan facilitar el acceso a empleos de colectivos desfavorecidos.  

        .col-10.bg-5
          .h6 Fundaciones
          p Son patrimonios autónomos que se destinan única y exclusivamente al objetivo establecido por sus fundadores. No tienen ánimo de lucro, no tienen un monto mínimo en su patrimonio y se regulan totalmente por su estatus.  

        .col-10.bg-5
          .h6 Sociedades laborales
